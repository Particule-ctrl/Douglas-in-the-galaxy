function initSprite(matrix) {
  img = createImage(matrix[0].length, matrix.length);
  img.loadPixels();
  for (let i = 0; i < img.width; i++) {
    for (let j = 0; j < img.height; j++) {
      if (matrix[j][i] != "#ffffff") {
        img.set(i, j, color(matrix[j][i]));
      } else {
        img.set(i, j, color(0, 0, 0, 0));
      }

    }
  }
  img.updatePixels();
  return img;
}

function initLev() {
  murTab = [];
  ennemiTab = [];
  pnjTab = [];

  for (var y = 0; y < niveauActu.length; y++) {
    for (var x = 0; x < niveauActu[y].length; x++) {
      if (niveauActu[y][x] == 1) {
        murTab.push(new Mur(x * tailleCarre, y * tailleCarre));
      }
      else if (niveauActu[y][x] == 2) {
        ennemiTab.push(new Ennemi(x * tailleCarre, y * tailleCarre));
      }
      else if (niveauActu[y][x] == 3) {
        pnjTab.push(new PNJ(x * tailleCarre, y * tailleCarre, "Arme"));
      }
      else if (niveauActu[y][x] == 4) {
        pnjTab.push(new PNJ(x * tailleCarre, y * tailleCarre, "Tuto"));
      }
      else if (niveauActu[y][x] == 5) {
        pnjTab.push(new PNJ(x * tailleCarre, y * tailleCarre, "TP"));
      }
      else if (niveauActu[y][x] == 6) {
        pnjTab.push(new PNJ(x * tailleCarre, y * tailleCarre, "Fin"));
      }
      else if (niveauActu[y][x] == "S") {
        spawnTab.push(new EnnemiKiller(x * tailleCarre, y * tailleCarre));


      }
    }
  }
  for (var i = 0; i < murTab.length; i++) {
    murTab[i].afficher();
  }
  for (var i = 0; i < ennemiTab.length; i++) {
    ennemiTab[i].afficher();
  }
  for (var i = 0; i < pnjTab.length; i++) {
    pnjTab[i].afficher();
  }
  for (var i = 0; i < spawnTab.length; i++) {
    spawnTab[i].spawnProof();
  }

}


